// Incremento    

let c = 3, d = 2;
let y = c + d;

        //pre-incremento

        y = ++c; 
        console.log(c);
        console.log(y);

        //post-incremento

        y = d++;
        console.log(d);
        console.log(y);


// Decremento

let e = 3, f = 2;
let x = c + d;

        // pre-decremento

        x = --e;
        console.log(e);
        console.log(x);

        // post-decremento

        y = f--;
        console.log(f);
        console.log(x);